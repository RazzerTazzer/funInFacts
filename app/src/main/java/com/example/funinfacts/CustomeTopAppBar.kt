@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun CustomTopAppBar(screen: String, navController: NavHostController) = if(screen == "Category") {
    CategoryListTopAppBar()
}
else {
    Log.d("TopApp", screen)
    CategoryDetailTopAppBar(screen, navController)

}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun CategoryDetailTopAppBar(screen: String, navController: NavHostController) {
    CenterAlignedTopAppBar(
        colors = TopAppBarDefaults.topAppBarColors(
            containerColor = MaterialTheme.colorScheme.primaryContainer,
            titleContentColor = MaterialTheme.colorScheme.primary,
        ),
        title = {
            Text(screen)
        },
        navigationIcon = {
            IconButton(onClick = { navController.popBackStack() }) {
                Icon(
                    imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                    contentDescription = "Localized description"
                )
            }
        },
        actions = {
            IconButton(onClick = {}) {
                Icon(
                    imageVector = Icons.Filled.Menu,
                    contentDescription = "Localized description"
                )
            }
        },

    )
}

